lenguajes = ['Python', 'JavaScript', 'Dart', 'C++', 'Ruby']

## Los iteradores corren determinado número de veces una pieza de código

#iterador
for lenguaje in lenguajes:
    print(f'Estoy aprendiendo {lenguaje}')

#for que escrina números
for num in range(0, 20):
    print(num)

##Le podemos decir que incremente en cierta cantidad de números
for num in range(0, 20, 3):
    print(num)
